import math
# a) 5 to the power of 8
power_result = 5 ** 8
# b) Square root of 400
sqrt_result = math.sqrt(400)
# c) Exponent of 5
exp_result = math.exp(5)
# d) Logarithm of 625 base 5
log_result = math.log(625, 5)
print("5 to the power of 8: ",power_result)
print("Square root of 400: ",sqrt_result)
print("Exponent of 5: ",exp_result)
print("Logarithm of 625 base 5:",log_result)
